"""The name pool — just strings, checked in.

Cooked offline from Faker across 26 locales, then hand-extended with four traditions
whose scripts machine transliteration cannot render legibly (see below). **Faker is not
a runtime dependency**: a generator library is a lot of machinery for what is, in the
end, two lists of words, and baking it in makes the distribution inspectable,
reviewable in a diff, and identical on every install.

Every entry is already canonical — ASCII, lowercase, a single token — so
generated names need no transliteration at all.

**Arabic, Persian, Hebrew and Tamil are hand-written.** The first three are
abjads: short vowels are not written, so machine transliteration produces
consonant clusters — Arabic for 'Muhammad' comes out as ``md_lfyfy``. Tamil
transliterates legibly but at 27 characters. An audit flagged exactly these
four, and the obvious fix — dropping them — was the wrong one, since it would
have removed those traditions from a pool whose whole purpose is even
distribution. These are the spellings such names take in Latin script,
which is precisely what a transliterator cannot know.

Given and family names are drawn **independently**, so most names cross traditions:
``rosemary_nasrin``, ``trevor_mahmood``. That is deliberate.
"""

